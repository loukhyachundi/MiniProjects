CREATE DATABASE IF NOT EXISTS DIGITAL_LIBRARY_SYSTEM;
USE DIGITAL_LIBRARY_SYSTEM;

CREATE TABLE IF NOT EXISTS LIBRARY_BOOKS (
    BOOK_ID INT PRIMARY KEY,
    BOOK_TITLE VARCHAR(100),
    GENRE VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS LIBRARY_MEMBERS (
    MEMBER_ID INT PRIMARY KEY,
    MEMBER_NAME VARCHAR(100),
    LAST_BORROW_DATE DATE
);

CREATE TABLE IF NOT EXISTS BOOK_TRANSACTIONS (
    TRANSACTION_ID INT PRIMARY KEY,
    BOOK_ID INT,
    MEMBER_ID INT,
    BORROW_DATE DATE,
    RETURN_DATE DATE,
    FOREIGN KEY (BOOK_ID) REFERENCES LIBRARY_BOOKS(BOOK_ID),
    FOREIGN KEY (MEMBER_ID) REFERENCES LIBRARY_MEMBERS(MEMBER_ID)
);

