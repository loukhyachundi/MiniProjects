-- Find overdue books and calculate fine
SELECT
    M.MEMBER_NAME,
    B.BOOK_TITLE,
    T.BORROW_DATE,
    GREATEST(DATEDIFF(CURDATE(), T.BORROW_DATE) - 14, 0) AS LATE_DAYS,
    GREATEST(DATEDIFF(CURDATE(), T.BORROW_DATE) - 14, 0) * 5 AS FINE_AMOUNT
FROM BOOK_TRANSACTIONS T
JOIN LIBRARY_MEMBERS M ON T.MEMBER_ID = M.MEMBER_ID
JOIN LIBRARY_BOOKS B ON T.BOOK_ID = B.BOOK_ID
WHERE T.RETURN_DATE IS NULL
  AND T.BORROW_DATE < CURDATE() - INTERVAL 14 DAY;

-- Category-wise borrowing report
SELECT
    B.GENRE,
    COUNT(*) AS BOOKS_ISSUED
FROM BOOK_TRANSACTIONS T
JOIN LIBRARY_BOOKS B ON T.BOOK_ID = B.BOOK_ID
GROUP BY B.GENRE
ORDER BY BOOKS_ISSUED DESC;

-- Remove inactive members (not borrowed for 3 years)
SET SQL_SAFE_UPDATES = 0;

DELETE FROM LIBRARY_MEMBERS
WHERE LAST_BORROW_DATE < CURDATE() - INTERVAL 3 YEAR;

SET SQL_SAFE_UPDATES = 1;